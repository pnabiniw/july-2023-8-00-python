# Methods are also a function but there is a difference in functions and methods

# append() method
nums = [1, 2, 3, 4]
nums.append(5)
print(nums)

# extend() method
num1 = [1, 2, 3]
num1.extend([4, 5, 6])
print(num1)

# insert() method
vowels = ['a', 'e', 'o', 'u']
vowels.insert(2, 'i')
print(vowels)

